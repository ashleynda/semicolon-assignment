package Estore;

public class Users {
    private String name;
    private int age;
    private String email;
    private String homeAddress;
    private String password;
    private String phone;

    public Users(String name, int age, String email, String homeAddress, String password, String phone){
        this.name = name;
        this.age = age;
        this.email = email;
        this.homeAddress = homeAddress;
        this.password = password;
        this.phone = phone;
    }
}
